import math

radius = float(input("Enter the radius of the circle: "))
area = math.pi * radius * radius
print("The area of the circle is: ", area)

# The area of a circle is given by the formula: area = π * radius^2
# where π (pi) is a constant approximately equal to 3.14159.
# The radius is the distance from the center of the circle to any point on its circumference.

